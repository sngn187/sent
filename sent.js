const cron = {
  /**
   * schedule a cron expression with Cambodia timezone
   * @param {string} timeExpr - cron expression like "30 10 * * *"
   * @param {Function} fn - callback to run
   * @param {Object} [options] - optional options included for compatibility
   * @returns {CronJob}
   */
  schedule: (timeExpr, fn, options) => {
    // Wrap the job callback with try/catch + log timestamps
    const wrapped = () => {
      console.log(
        `[CRON] "${timeExpr}" triggered at ${new Date().toISOString()}`
      );
      try {
        const maybePromise = fn();
        // if fn returns a Promise, log resolution or rejection
        if (maybePromise && typeof maybePromise.then === "function") {
          maybePromise
            .then(() => {
              console.log(
                `[CRON] "${timeExpr}" callback finished successfully.`
              );
            })
            .catch((err) => {
              console.error(
                `[CRON] "${timeExpr}" callback ERROR (async):`,
                err
              );
            });
        }
      } catch (err) {
        console.error(`[CRON] "${timeExpr}" callback ERROR:`, err);
      }
    };

    const job = new CronJob(timeExpr, wrapped, null, true, "Asia/Phnom_Penh");
    // immediate start if not auto-started (CronJob constructor with true starts it)
    return job;
  },
};

